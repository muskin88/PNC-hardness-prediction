# ============================================================
# 2. FEATURES
# ============================================================

df['k'] = df['G'] / df['B']
df['sigma'] = df['G'] * df['Covalency']  # Σ = G × C
df['delta'] = 0.001  # Δ ≈ 0 (statistically insignificant)

# Tian model for comparison
df['Hv_Tian'] = 0.92 * (df['k']**1.137) * (df['G']**0.708)

# ============================================================
# 3. PNC MODEL
# ============================================================

print("\n" + "=" * 60)
print("5-FOLD CROSS-VALIDATION")
print("=" * 60)

kf = KFold(n_splits=5, shuffle=True, random_state=42)
pn_maes, pn_rmses, pn_corrs = [], [], []
gammas = []

for fold, (train_idx, test_idx) in enumerate(kf.split(df)):
    train, test = df.iloc[train_idx].copy(), df.iloc[test_idx].copy()

    # Optimize γ
    def neg_corr(gamma):
        K = train['sigma'] / (train['delta'] + gamma)
        return -abs(np.corrcoef(K, train['Hv'])[0, 1])

    result = minimize_scalar(neg_corr, bounds=(0.01, 500), method='bounded')
    gamma = result.x
    gammas.append(gamma)

    train['K'] = train['sigma'] / (train['delta'] + gamma)
    test['K'] = test['sigma'] / (test['delta'] + gamma)

    lr = LinearRegression()
    lr.fit(train[['K']], train['Hv'])
    pred = lr.predict(test[['K']])

    pn_maes.append(mean_absolute_error(test['Hv'], pred))
    pn_rmses.append(np.sqrt(mean_squared_error(test['Hv'], pred)))
    pn_corrs.append(np.corrcoef(test['Hv'], pred)[0, 1])

    print(f"Fold {fold+1}: γ = {gamma:.1f}, MAE = {pn_maes[-1]:.2f} GPa")

# Tian comparison
tian_maes = []
for train_idx, test_idx in kf.split(df):
    test = df.iloc[test_idx]
    tian_maes.append(mean_absolute_error(test['Hv'], test['Hv_Tian']))

